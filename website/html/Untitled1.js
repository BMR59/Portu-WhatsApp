function contraction()
{
var chars = document.getElementsByClassName ("contraction")
if (chars[0].style.color != "#FF3300")
{
for (var i = 0, length = chars.length; i < length; i++) {
            chars[i].style.color = "#FF3300";
}
}
else {
  for (var i = 0, length = chars.length; i < length; i++) 
{chars[i].style.color = "";}  
}
}
